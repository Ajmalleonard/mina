import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import sharp from "sharp";

const appRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const publicDir = path.join(appRoot, "public");
const generatedDir = path.join(appRoot, "src", "generated");
const manifestPath = path.join(generatedDir, "image-manifest.ts");

const RASTER_EXTENSIONS = new Set([".jpg", ".jpeg", ".png", ".webp"]);
const OPTIMIZE_SOURCE_EXTENSIONS = new Set([".jpg", ".jpeg", ".png"]);
const LARGE_IMAGE_THRESHOLD_BYTES = 150 * 1024;
const MAX_OPTIMIZED_WIDTH = 1800;
const PLACEHOLDER_SIZE = 24;

function ensureDirectory(dirPath) {
  fs.mkdirSync(dirPath, { recursive: true });
}

function toPosixPath(filePath) {
  return filePath.split(path.sep).join(path.posix.sep);
}

function collectFiles(dirPath, files = []) {
  const entries = fs.readdirSync(dirPath, { withFileTypes: true });

  for (const entry of entries) {
    if (entry.name === "_optimized") {
      continue;
    }

    const fullPath = path.join(dirPath, entry.name);

    if (entry.isDirectory()) {
      collectFiles(fullPath, files);
      continue;
    }

    const extension = path.extname(entry.name).toLowerCase();

    if (RASTER_EXTENSIONS.has(extension)) {
      files.push(fullPath);
    }
  }

  return files;
}

async function ensurePlaceholder(outputPath) {
  if (fs.existsSync(outputPath)) {
    return;
  }

  ensureDirectory(path.dirname(outputPath));

  await sharp({
    create: {
      width: 1200,
      height: 800,
      channels: 3,
      background: "#ece7da",
    },
  })
    .jpeg({ quality: 72, mozjpeg: true })
    .toFile(outputPath);
}

async function createOptimizedImage(sourcePath, optimizedPath) {
  ensureDirectory(path.dirname(optimizedPath));

  const sourceStats = fs.statSync(sourcePath);
  const optimizedExists = fs.existsSync(optimizedPath);

  if (optimizedExists) {
    const optimizedStats = fs.statSync(optimizedPath);

    if (optimizedStats.mtimeMs >= sourceStats.mtimeMs) {
      return;
    }
  }

  await sharp(sourcePath)
    .rotate()
    .resize({
      width: MAX_OPTIMIZED_WIDTH,
      height: MAX_OPTIMIZED_WIDTH,
      fit: "inside",
      withoutEnlargement: true,
    })
    .webp({ quality: 74, effort: 6 })
    .toFile(optimizedPath);
}

async function createBlurDataUrl(sourcePath) {
  const buffer = await sharp(sourcePath)
    .rotate()
    .resize({
      width: PLACEHOLDER_SIZE,
      height: PLACEHOLDER_SIZE,
      fit: "inside",
      withoutEnlargement: true,
    })
    .webp({ quality: 40 })
    .toBuffer();

  return `data:image/webp;base64,${buffer.toString("base64")}`;
}

async function buildManifest() {
  await ensurePlaceholder(path.join(publicDir, "placeholder.jpg"));
  await ensurePlaceholder(path.join(publicDir, "images", "placeholder.jpg"));

  const manifest = {};
  const files = collectFiles(publicDir).sort();

  for (const filePath of files) {
    const publicRelativePath = toPosixPath(path.relative(publicDir, filePath));
    const publicPath = `/${publicRelativePath}`;

    try {
      const extension = path.extname(filePath).toLowerCase();
      const stats = fs.statSync(filePath);
      const metadata = await sharp(filePath).rotate().metadata();
      const blurDataURL = await createBlurDataUrl(filePath);

      let optimizedSrc = publicPath;

      if (
        OPTIMIZE_SOURCE_EXTENSIONS.has(extension) &&
        stats.size >= LARGE_IMAGE_THRESHOLD_BYTES
      ) {
        const optimizedRelativePath = toPosixPath(
          path.join(
            "_optimized",
            publicRelativePath.replace(/\.(jpe?g|png)$/i, ".webp")
          )
        );
        const optimizedPath = path.join(publicDir, optimizedRelativePath);

        await createOptimizedImage(filePath, optimizedPath);
        optimizedSrc = `/${optimizedRelativePath}`;
      }

      manifest[publicPath] = {
        src: optimizedSrc,
        width: metadata.width ?? 1200,
        height: metadata.height ?? 800,
        blurDataURL,
      };
    } catch (error) {
      console.warn(`Skipping image ${publicPath}: ${error.message}`);
    }
  }

  const manifestSource = `// This file is generated by scripts/generate-image-manifest.mjs\n` +
    `// Do not edit it by hand.\n\n` +
    `export const imageManifest = ${JSON.stringify(manifest, null, 2)} as const;\n`;

  ensureDirectory(generatedDir);
  fs.writeFileSync(manifestPath, manifestSource);
}

buildManifest().catch((error) => {
  console.error("Failed to generate image manifest:", error);
  process.exitCode = 1;
});
